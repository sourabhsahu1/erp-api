<?php


namespace Modules\Hr\Repositories;


use Luezoid\Laravelcore\Exceptions\AppException;
use Luezoid\Laravelcore\Repositories\EloquentBaseRepository;
use Modules\Hr\Models\AddressType;
use Modules\Hr\Models\EmployeeAddress;

class AddressTypeRepository extends EloquentBaseRepository
{
    public $model = AddressType::class;

    public function delete($data)
    {
        $empAddress = EmployeeAddress::where('address_type_id', $data['id'])->first();
        if (is_null($empAddress)) {
            return parent::delete($data);
        }else {
            throw new AppException('Already in use');
        }
    }
    public function getAll($params = [], $query = null)
    {

        if (isset($params["inputs"]["orderby"])) {
            $params["inputs"]["orderby"] = 'name';
            $params['inputs']['order'] = 'asc';
        }

        return parent::getAll($params, $query); // TODO: Change the autogenerated stub
    }
}
